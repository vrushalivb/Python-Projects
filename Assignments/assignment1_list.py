"""18. Write a python function that receives a list of integers and prints out a histogram of bin size 10

	Input:
	[13,42,15,37,22,39,41,50]
	Output:
	{11-20:2,21-30:1,31-40:2,41-50:3}
"""

def print_histogram(lst):
    if not lst:
        return {}

    # Sorting data to make it easy to range it out
    sorted_data = sorted(lst)

    # Calculating start and end boundaries for the bins
    # For example, 13 starts at 11 (10 + 1)
    start = (min(sorted_data) - 1) // 10 * 10
    end = (max(sorted_data) + 9) // 10 * 10

    histogram = {}

    # Iterate through the range and create bins
    for i in range(start, end, 10):
        bin_label = f"{i + 1}-{i + 10}"
        # Count elements that fall within the current bin
        count = len([x for x in sorted_data if i < x <= i + 10])

        # Only add bins that have at least one element
        if count > 0:
            histogram[bin_label] = count

    return histogram


sample_list = [13, 42, 15, 37, 22, 39, 41, 50]
print_histogram(sample_list)