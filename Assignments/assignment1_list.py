"""7. Sort a list of alphanumeric strings based on product value of numeric character in it. If in any string there is no numeric character take it's product value as 1.

	Input:

	['1ac21', '23fg', '456', '098d','1','kls']
	Output:

	['456', '23fg', '1ac21', '1', 'kls', '098d']
"""

def sort_alpha_numeric_list(lst):
    # assigning 1 as product value
    p = 1
    has_digit = False

    # Iterating one by one character to find it has digit and multiply digits
    for char in lst:
        if char.isdigit():
            p = p * int(char)
            has_digit = True

    # If no digits found in string, the product remains 1
    return p


alpha_numeric_list = ['1ac21', '23fg', '456', '098d', '1', 'kls']

# Sorting in descending order
sorted_data = sorted(alpha_numeric_list, key=sort_alpha_numeric_list, reverse=True)
