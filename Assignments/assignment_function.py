"""6. Write a Python function that accepts a string and counts the number of upper and lower case letters.
	Sample String : 'The quick Brow Fox'
	Expected Output :
	No. of Upper case characters : 3
	No. of Lower case Characters : 12
"""

def string_upper_lower(my_string):
  upper_count = 0
  lower_count = 0

  # iterating one by one character till last character of string
  for char in my_string:
    # checking characters are in upper case
    if char.isupper():
      # counting one by one upper case characters
      upper_count += 1
      #also checking characters are in lower case
    elif char.islower():
      # counting one by one lower case characters
      lower_count += 1
      # returning result
  return f"No. of Lower case characters: {lower_count} No of Upper case characters: {upper_count}"


# calling function
string_upper_lower("The quick Brow Fox")

"""7. Write a Python function that takes a list and returns a new list with distinct elements from the first list.

	Sample List : [1,2,3,3,3,3,4,5]
	Unique List : [1, 2, 3, 4, 5]
"""

def distinct_list(my_list):
  new_list = []

  # iterating elements one by one till last element of list
  for i in my_list:
    # adding into new list
    new_list.append(i)
  return new_list

