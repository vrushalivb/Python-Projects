"""9. Write a Python program that accepts a hyphen-separated sequence of words as input and prints the words in a hyphen-separated sequence after sorting them alphabetically.

	Sample Items : green-red-yellow-black-white
	Expected Result : black-green-red-white-yellow
"""

def hyphen_separated_words(my_string):
  # spliting input string by '-' character
  break_string = my_string.split("-")
  # sorting in ascending order
  break_string.sort()
  # joining with '-' character after sorting
  joint_myString = "-".join(break_string)
  # returning result
  return joint_myString


# calling function
hyphen_separated_words("green-red-yellow-black-white")

