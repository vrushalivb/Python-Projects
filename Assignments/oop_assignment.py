"""17. Create a Bus child class that inherits from the Vehicle class. The default fare charge of any vehicle is seating capacity * 100. If Vehicle is Bus instance, we need to add an extra 10% on full fare as a maintenance charge. So total fare for bus instance will become the final amount = total fare + 10% of the total fare.

Note: The bus seating capacity is 50. so the final fare amount should be 5500. You need to override the fare() method of a Vehicle class in Bus class.
"""

# A base class
class Vehicle:

  #  initializing name, mileage
    def __init__(self, name, mileage, capacity):
        self.name = name
        self.mileage = mileage
        self.capacity = capacity

    def fare(self):
        return self.capacity * 100 #5000


class Bus(Vehicle):
    # Overriding the fare() method of Vehicle class
    def fare(self):
        # getting the base fare() from the parent Vehicle class
        base_fare = super().fare()
        # Adding 10% maintenance charge
        final_amount = base_fare + (base_fare * 0.10)
        return final_amount


